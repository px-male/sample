package model;

public class Mutter {
	
	private String name;
	private String content;
	
	public Mutter() {
		
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getContent() {
		return content;
	}
	public void setString(String content) {
		this.content = content;
	}
	
}
